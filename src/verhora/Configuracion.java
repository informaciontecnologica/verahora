    private void BActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BActualizarActionPerformed
        registro.PortServidor = Integer.parseInt(this.Tpuerto.getText());
        registro.ipServidor = TIpServidor.getText();
        registro.HumMin = Integer.parseInt(this.THumMini.getText());
        registro.HumMAx = Integer.parseInt(this.THumMax.getText());

        registro.TempMin = Integer.parseInt(this.TGradosMini.getText());
        registro.TempMax = Integer.parseInt(this.TGradosmax.getText());
        registro.Refresco = Integer.parseInt(this.TRefresco.getText());

        try {
            s.setReConfiguracion(registro);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Configuracion.class.getName()).log(Level.SEVERE, null, ex);
        }

            
            re.Stop();

    }//GEN-LAST:event_BActualizarActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        try {
            s.getReConfiguracion(registro);
        } catch (IOException e) {
            System.out.println("Excepci√≥n " + e.getMessage());
        }
        this.TIpServidor.setText(s.getIpServidor());
        this.Tpuerto.setText(String.valueOf(s.getPortServidor()));
        this.THumMini.setText(String.valueOf(s.getHumMin()));
        this.THumMax.setText(String.valueOf(s.getHumMAx()));
        this.TGradosMini.setText(String.valueOf(s.getTempMin()));
        this.TGradosmax.setText(String.valueOf(s.getTempMax()));
        this.TRefresco.setText(String.valueOf(s.getRefresco()));
    }//GEN-LAST:event_formWindowActivated

